# 5)და კომენტარებით ახსენი რა არის for loop


# for loop არის ციკლი არის იტერაციის ერთ-ერთი მაგალითი რომლის დროსაც ვიყენებთ ცვლადს, საწყის რიცხვს, სასრულს და ნაბიჯს რომელსაც for loop-ი გაივლის 

for i in range (0, 10, 1):
    print (i)

 # for - არის key work ციკლის 
# i - არის საიტერაციო ცვლადის სახელი
# range - დიაპაზონი
# (0 , 10 , 1)
# 0 - საწყისი
# 10 - სასრული 
# 1 - ნაბიჯები, რამდენის შემდეგ დაბეჭდავს
