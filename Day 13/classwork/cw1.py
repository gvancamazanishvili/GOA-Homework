# 1) გააკეთეთ პატარა გულის ცემის სისტემა რომელიც ითვლის რა თქმა უნდა გულის ცემას  თუ 180 ზე მეტია ამ შემთხვევაში მაღალია თუ 160-დან 170 მდე არის ამ შემთვევაში არის ნორმალური თუ 160-ზე ნაკლებია ამ შემთხვევაში დაბალია

pulse = int(input("enter your pulse: "))


if pulse > 180:
    print("მაღალია")
elif pulse > 160:
    print("ნორმალურია")
else:
    print("ნაკლებია")
